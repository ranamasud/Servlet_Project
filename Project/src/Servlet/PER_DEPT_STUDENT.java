	public void doPost(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {

		String dept= request.getParameter("dept");
		
		if(dept!=null)
		{
			
		ServiceClass sj = new ServiceClass();
		
		int stu_num=sj.Stu_num(dept);
		
		PRT ob=new PRT();
		
		ob.setNUM(stu_num);
		
		request.setAttribute("tab", ob);
		
		request.getRequestDispatcher("per_dept_stu.jsp").forward(request, response);
		}
		else
		{
			request.getRequestDispatcher("ddddd.jsp").forward(request, response);	
		}
		
	}

