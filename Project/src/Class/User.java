package Class;

public class User {

	private String Name;
	private String Id;
	private String Email;
	private String Pass;
	private String Dept;
	private String CGPA;
	private int NUM;
	public void setName(String name) {
		this.Name = name;
	}


	public String getName() {
		return Name;
	}
	
	
	public void setId(String id) {
		this.Id = id;
	}


	public String getId() {
		return Id;
	}
	
	
	
	
	public void setEmail(String email) {
		this.Email = email;
	}


	public String getEmail() {
		return Email;
	}
	
	
	public void setPass(String pass) {
		this.Pass = pass;
	}

	public String getPass() {
		return Pass;
	}
	
	
	public void setDept(String dept) {
		this.Dept = dept;
	}


	public String getDept() {
		return Dept;
	}

	
	public void setCGPA(String CGPA) {
		this.CGPA = CGPA;
	}


	public String getCGPA() {
		return CGPA;
	}
	
	
	public void setNUM(int num) {
		this.NUM = num;
	}


	public int getNUM() {
		return NUM;
	}

	
	
	
	
}
