package Class;

public class SIR {

	private String Name;
	private String Dept;
	private String Pass;
	private int NUM;
	public void setName(String name) {
		this.Name = name;
	}


	public String getName() {
		return Name;
	}
	
	public void setDept(String dept) {
		this.Dept = dept;
	}


	public String getDept() {
		return Dept;
	}
	
	public void setPass(String pass) {
		this.Pass = pass;
	}


	public String getPass() {
		return Pass;
	}
	
	
	public void setNUM(int num) {
		this.NUM = num;
	}


	public int getNUM() {
		return NUM;
	}

	
	
}
